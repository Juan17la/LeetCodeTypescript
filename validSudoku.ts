function isValidSudoku(board: string[][]): boolean {
  let seen = new Set<string>();

  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      let num = board[i][j];
      if (num !== ".") {

        let rowKey = num + " in row " + i;
        let colKey = num + " in col " + j;
        let blockKey = num + " in block " + Math.floor(i/3) + "_" + Math.floor(j/3);

        if (seen.has(rowKey) || seen.has(colKey) || seen.has(blockKey)) {
          return false;
        }

        seen.add(rowKey);
        seen.add(colKey);
        seen.add(blockKey);
      }
    }
  }

  return true;
}


